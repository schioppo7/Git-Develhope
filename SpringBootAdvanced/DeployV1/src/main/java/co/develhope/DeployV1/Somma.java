package co.develhope.DeployV1;

public class Somma {
    private int a;
    private int b;
    private int somma;

    public Somma(int a, int b, int somma) {
        this.a = a;
        this.b = b;
        this.somma = somma;
    }

    public int getA() {
        return a;
    }

    public int getB() {
        return b;
    }

    public int getSum() {
        return somma;
    }
}
